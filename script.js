function abrirmenu1() {
  let PrimeiroMenu = document.getElementById("small-um");
  if (PrimeiroMenu.style.display == "none") {
    PrimeiroMenu.style.display = "block";
  } else {
    PrimeiroMenu.style.display = "none";
  }
}
